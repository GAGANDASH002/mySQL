-- NUMERIC FUNCTIONS
SELECT 2 + 3 AS RESULT;
SELECT 2-3 AS RESULT;
SELECT 2*3 AS RESULT;
SELECT 2/3 AS RESULT;

-- PI FUNCTION
SELECT PI() AS RESULT;
-- POWER
SELECT POW(10,2) AS RESULT;
-- RAND-GENERATES RANDOM NUMBER FROM RANGE 0 TO 1 
SELECT RAND() AS RESULT;
-- ROUND - RETURNS ROUNDED VALUE
SELECT ROUND(7.46) AS RESULT;
-- SIGN - RETURN SIGN OF THE EXPRESSION
SELECT SIGN(100) AS RESULT;
SELECT SIGN(-100) AS RESULT;
-- SQRT() -- RETURNS SQRT OF POSITIVE NUMBERS ONLY
SELECT SQRT(65) AS RESULT;

